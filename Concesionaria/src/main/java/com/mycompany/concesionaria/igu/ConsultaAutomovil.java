    private void cargarTabla() {
        //Hacemos que la tabla no sea editable
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //titulos a las columnas
        String titulos[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Pantente", "Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        
        //Traer los autos desde la BD
        List <Automovil> listaAutomoviles = controlLog.traerAutos();
        
        //setear los datos en la tabla
            if(listaAutomoviles != null){
                for(Automovil auto: listaAutomoviles){
                    Object objeto[] = {auto.getId(), auto.getModelo(), auto.getMarca(), auto.getMotor(),
                                       auto.getColor(),auto.getPatente(), auto.getCantPuertas()};
                
                    modeloTabla.addRow(objeto);
                
                }
            }
        
        tablaAutos.setModel(modeloTabla);
    }
    
    
